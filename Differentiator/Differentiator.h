/**============================================================================
 *                           Rules of differentiation
 *
 *                           1)Sum
 *                          (f +(-) g)' = (f)' +(-) (g)'
 *
 *                                It looks like
 *
 *                                   ------
 *                                   |+(-)|
 *                                   ------
 *                                  /      \
 *                                 /        \
 *                               ---       ---
 *                               |f|       |g|
 *                               ---       ---
 *
 *                                     ||
 *                                     \/
 *
 *                                   ------
 *                                   |+(-)|
 *                                   ------
 *                                  /      \
 *                                 /        \
 *                             ------      ------
 *                             |(f)'|      |(g)'|
 *                             ------      ------
 *
 *                           2)Multiplitation
 *                         (f*g)' = (f)' * g + f * (g)'
 *
 *                                It looks like
 *
 *                                     ---
 *                                     |*|
 *                                     ---
 *                                    /   \
 *                                   /     \
 *                                 ---     ---
 *                                 |f|     |g|
 *                                 ---     ---
 *
 *                                     ||
 *                                     \/
 *
 *                                     ---
 *                                     |+|
 *                                     ---
 *                                  __/   \ __
 *                                /           \
 *                              ---           ---
 *                   (firstLeft)|*|           |*|(firstRight)
 *                              ---           ---
 *                             /  \          /  \
 *                            /    \        /    \
 *                        ------   ---    ---    ------
 *        (secondLeftLeft)|(f)'|   |g|    |f|    |(g)'|(secondRightRight)
 *                        ------   ---    ---    ------
 *
 *                           3)Divide
 *                    (f/g)' = ((f)' * g - f * (g)') / (g * g)
 *
 *                                It looks like
 *
 *                                     ---
 *                                     |/|
 *                                     ---
 *                                    /   \
 *                                   /     \
 *                                 ---     ---
 *                                 |f|     |g|
 *                                 ---     ---
 *
 *                                     ||
 *                                     \/
 *
 *                                     ---
 *                                     |/|
 *                                     ---
 *                          __________/   \ ________
 *                        /                         \
 *                      ---                        ---
 *                      |-| (firstLeft)            |*|(firstRight)
 *                      ---                        ---
 *                  ___/   \___                   /  \
 *                 /           \                 /    \
 *               ---           ---             ---    ---
 *               |*|           |*|             |g|    |g|
 *               ---           ---             ---    ---
 *              /  \          /  \
 *             /    \        /    \
 *         ------   ---    ---    ------
 *         |(f)'|   |g|    |f|    |(g)'|(thirdLeftRightRight)
 *         ------   ---    ---    ------
 *
 *                           4)Power
 *                  (f^g)' = (f^g) * ((g)' * ln(f) + (g/f) * (f)')
 *
 *                                It looks like
 *
 *                                     ---
 *                                     |^|
 *                                     ---
 *                                     / \
 *                                    /   \
 *                                  ---   ---
 *                                  |f|   |g|
 *                                  ---   ---
 *
 *                                     ||
 *                                     \/
 *
 *                                     ---
 *                                     |*|
 *                                     ---
 *                          __________/   \ ________
 *                        /                         \
 *                      ---                        ---
 *                      |+|                        |^|(firstRight)
 *                      ---                        ---
 *                  ___/    \ _                   /  \
 *                 /           \                 /    \
 *               ---           ---             ---    ---
 *               |*|           |*|             |f|    |g|(secondRightRight)
 *               ---           ---             ---    ---
 *              /  \          /  \
 *             /    \        /    \
 *         ------   ----   ---    ------
 *         |(g)'|   |ln|   |/|    |(f)'|(thirdLeftRightRight)
 *         ------   ----   ---    ------
 *                   |      |\
 *                   |      | \
 *                   |      |  \
 *                   |      |   \
 *                  ---    ---  ---
 *                  |f|    |g|  |f|(forthLeftRightLeftRight)
 *                  ---    ---  ---
 *
 *
 *
 *=============================================================================
 */


